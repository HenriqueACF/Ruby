# VARIAVEL LOCAL
# pode ser acessada apenas onde foi criada

# VARIAVEL GLOBAL
# pode ser acessada em qualquer lugar do programa
# é declarada com o prefixo $

# VARIAVEL DE CLASSE
# pode ser acessada em qualquer lugar da classe
# é declarada com o prefixo @@

# VARIAVEL DE INSTANCIA
# semelhante a variavel de classe, tendo a diferença de que o valor nao é compartilhado entre todas as instancias de sua classe
# é declarado com o prefixo @


